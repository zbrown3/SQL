SELECT name FROM celebs;
-- select is a clause that indicates that the statement is a query.  You will use select every time you query data from the database.
-- name specifies the column to query from.
-- FROM celebs specifies the name of the table to query data from.  

SELECT * FROM celebs;
-- * is a special wildcard character that allows you to select every column in a table without having to name each one individually.
-- SELECT statements always return a new table called the result set. 